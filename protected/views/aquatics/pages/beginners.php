<h4 id="cap">Choosing the right Swimming caps</h4>
<p>A swim cap is an important part of many swimmers workout attire. Swim caps are worn for many purposes: to protect your hair from chlorine, keep your hair from tangling, to help streamline you as you swim, to insulate the top of your head when in cold water, during an open water swim to make you visible, or can be worn simply as a fashion item to make a statement with a printed logo or have your cap match your swim suit.</p>
<p>There are several major types of swim caps:</p>
<ul>
	<li>
	<b>LATEX RACING CAPS</b> are the most popular type of swim cap. They are named for the latex rubber material used in their construction. Latex caps are very popular because they provide good protection from the water and are extremely inexpensive. They are available in a wide variety of colors, and can be printed with neat designs or team names. They are very streamlined and make you more slippery in the water. And because they fit very tight to the head, when you pull them down and wear them over your ears they provide relatively good protection from the water, keeping water off of most of your hair and out of your ears.</li>
	
	<li><b>SILICONE CAPS</b> are a heavier weight material than latex, so these caps are much more durable than the standard latex racing cap (some swimmers get years of use from their silicone caps when rinsed properly after each swim) . Silicone caps have a slippery touch, so they do not pull your hair as much as other caps when you take 	the cap off of your head.</li>
	
	<li><b>LYCRA CAPS</b> are made of the same lycra® brand spandex as swim suits are. Consequently they will keep no water out of your hair. Lycra® caps are worn by people who want to keep the hair in place (and out of eyes and ears) and prevent their hair from being tangled on shoulder straps. Some swimmers wear these caps under a latex racing cap to reduce hair pulling when the swimmer takes their cap off. Also, since these caps are made from the same Lycra® as swim suits, it is possible to find a lycra swim cap that matches swimsuit colors for a fully coordinated outfit.</li>
	
	<li><b>INSULATED CAPS</b> help keep your head warm when swimming in cold water. The Barracuda Hot head cap has thick insulated construction and covers the full head and ears. These caps also usually are worn in a bright orange color which provides visibility when you are swimming in open waters.</li>
	
	<li><b>STRAP CAPS</b> are a solid molded rubber cap with molded ear flaps and a strap that fits under the chin. This strap helps keep the cap in place, and helps keep the ear flaps down to keep water away from the ears and hair. Very traditional looking, these caps are extremely popular and are great sellers.</li>
</ul>

