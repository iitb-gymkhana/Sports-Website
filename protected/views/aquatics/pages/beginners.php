<h4 id="breast">Breaststroke</h4>
<ul>
	<li>Head – Varies slightly, but primarily in-line with spine</li>
	<li>Eyes – Looking at the bottom of the pool when not breathing, looking down and 
	forward when breathing</li>
	<li>Shoulders and chest – Tip up and down/forward about a line drawn from one hip to 
	the other. Shoulders also lift and squeeze in towards ears as elbows come together 
	and hands/arms move forward in recovery</li>
	<li>Arms – Arms are a mirror of each other. Arm speed controls foot speed, and rhythm 
	will generally change during the course of a race, with the highest tempo occurring 
	during the first and last portion of a race.</li>
	<li>Forearm and hand pull – Propulsive surface starts as hand through shoulder as the 
	arms sweep out and up, then changes to hands through elbow as hands sweep 
	towards each other as elbows squeeze together</li>
	<li>Forearm and hand recovery – Hands lead forward at or under the surface of the 
	water, reaching forward; as the extend the upper body lowers forward and onto or 
	under the water, but avoid a diving down action</li>
	<li>Forearm and hand entry – Full extension, hands touching each other at the thumb</li>
	<li>Trunk – Must maintain the connection between shoulders and hips</li>
	<li>Hips – Hips are driven forward and under trunk by pull and by back muscles, then 
	become an anchor point as upper body launches forward with no pause as kick is 
	initiated.</li>
	<li>Hips remain relatively high in water, acting as a moving-forward pivot point</li>
	<li>Legs – Fast motion of ankles both up and towards the hips (kick recovery) and 
	through the kick action (back and slightly out to full extension)• Feet – Foot rhythm controlled by pull speed; feet must always move fast – foot speed always high, in both directions; kick concludes with legs in full extension, soles of feet pressing together</li>
	<li>Breathing – One breath per cycle, as hands and elbows sweep in, trunk rises, breath 
	taken from when mouth clears until trunk begins to lower.</li>
</ul>

