<h4 id="back">Backstroke</h4>
<ul>
	<li>Head – In-line with spine</li>
	<li>Eyes – Looking straight up in longer events and slightly back towards feet in 
	shorter events</li>
	<li>Shoulders and chest – Rotate around central axis of spine in same plane as hips. 
	High shoulder is on recovery arm side, low shoulder on pulling arm side</li>
	<li>Arms – Arms are always 180 degrees to each other. Arm speed is controlled by 
	kick speed. Recovery arm is straight from wrist to shoulder and points straight 
	up</li>
	<li>Forearm and hand pull – Propulsive surface, line from fingertips through wrist and elbow starts pointing towards the bottom and side of the pool, rotates more towards the side and surface of the pool, then finishes releasing the water towards the feet and under the hips; reach towards the bottom, then arm-wrestle, then throw something in your pocket</li>
	<li>Forearm and hand recovery – Straight arm from shoulder to wrist</li>
	<li>Forearm and hand entry – Pinky first, directly above and just outside the 
	shoulder with arm fully extended, with body rotation allowing entry hand to 
	reach deep into the catch</li>
	<li>Trunk – Must maintain the connection between the shoulders and the hips</li>
	<li>Hips – Rotate around central axis of spine in same plane as shoulders</li>
	<li>Attempt to initiate body rotation from the hips</li>
	<li>Legs – Kick is steady 6-beat, with faster tempo resulting in faster hands</li>
	<li>Feet – Faster feet = faster pull = faster swim. Boiling water at toes is not only 
	OK but encouraged to facilitate better follow-through on the kick</li>
	<li>Breathing – One breath per cycle; inhale on one pull, exhale on the next pull</li>
</ul>

