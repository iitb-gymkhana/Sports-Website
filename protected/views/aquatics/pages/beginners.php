<h4 id="free">Freestyle</h4>
<ul>
	<li>Head – In-line with spine</li>
	<li>Eyes – Looking at bottom of pool when not breathing, looking sideways, one eye directly 
	over/above the other while breathing.</li>
	<li>Shoulders and chest – Rotate around central axis of spine in the same plane as hips. 
	High shoulder is on recovery arm side, the low one is on the pulling arm side.</li>
	<li>Arms – One arm tends to be leading at all times, other arm is pulling or recovering; 
	more of a catch-up with hands overlapping in the area from shoulders forward – 
	sometimes called the front end or front quadrant. Shorter max-effort or sprint-efforts 
	may have less front-end overlap, even no front-end overlap but that is a function of the 
	timing. Not an effort to remove the overlap.</li>
	<li>Forearm and hand pull – Propulsive surface, line from fingertips up through wrist and 
	elbow should be pointing at the bottom of the pool as much of the time as possible – as 
	early as a catch can be achieved with the maximum possible catch held until the hand 
	passes below the waist</li>
	<li>Forearm and hand recovery – Relaxed, hanging from the elbow, swung forward rather 
	than pushed forward, thumb leading</li>
	<li>Forearm and hand entry – Fingertip first, rotated thumb up, pinky first entry, then a 
	gentle slide forward to a natural extension as the body rotates</li>
	<li>Trunk – Must maintain the connection between shouders and hips</li>
	<li>Hips – Rotate around central axis of spine in the same plane as shoulders</li>
	<li>Attempt to initiate body rotation from hips</li>
	<li>Legs – Kick is secondary to the pull, not overpowering it. It should be natural, not forced. 
	A variety of kick rhythms are acceptable, from 2-beat through 6-beat, with cross-over 
	variations.</li>
	<li>Feet – Faster feet = faster pull = faster swim if same kick pattern is maintained (2-beat, 
	6-beat etc.). Switching from a lower beat kick to a higher one will also result in faster arms and a faster swim, but possibly at a higher cost if not practiced often. Splashing is 
	not only OK but also encouraged to facilitate better follow-through on the kick.</li>
	<li>Breathing – Every 2 or 3 pulls – every 1 or 1.5 cycles. Swimmers must be equally 
	comfortable breathing to either side, May breathe less often for shorter events.</li>
</ul>

