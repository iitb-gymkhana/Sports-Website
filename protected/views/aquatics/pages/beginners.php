<h4 id="butterfly">Butterfly</h4>
<ul>
	<li>Head - Varies slightly, but primarily in-line with spine</li>
	<li>Eyes - Looking at bottom of bool when not breathing, looking down and forward when 
	breathing</li>
	<li>Shoulders and chest – Tip up and down/forward about a line drawn from one hip 
	through the other. The chest lays forward and presses down as the hands enter.</li>
	<li>Arms – Arms are a mirror of each other. Arm speed controls rhythm, with generally 
	steady to decreasing/slowing rhythm through the course of a race unless well 
	conditioned.</li>
	<li>Forearm and hand pull – Arms remain shoulder width apart, flex at elbows with line 
	from fingertips through elbow moving towards, pointing down and slightly inward, then 
	sweep inwards to outwards they press on the water, with the fingertip through elbow 
	line moving from pointing inwards and down to slightly outwards and down</li>
	<li>Forearm and hand recovery – Straight arms, relaxed hands, with a wide and low arm 
	swing, thumb down, pinky up, back of the hand towards thumb lead.</li>
	<li>Forearm and hand entry – Straight arms, extended, thumb to fingertip first, shoulder 
	width apart</li>
	<li>Trunk – Must maintain the connection between shoulders and hips</li>
	<li>Hips – Maintain hips relatively high in water, acting as a moving forward pivot point</li>
	<li>Legs – Generally two kicks per cycle, but one per cycle is also acceptable. Timing is 
	based on arm timing, with kick balancing arm and trunk motion. There is generally a kick 
	as the pull begins and second kick just prior to the hand exit. The kick action and the 
	chest position must together or swimmer will be moving body up and down instead of 
	forward. If swimmer attempts to kick with too much effort they will tire sooner than if 
	they allow the kick to work with the arms and body</li>
	<li>Feet – Feet must remain in the same plane throughout the entire kick. They may be in 
	slightly different planes, but that difference should not change. Feet are extended, 
	relaxed ankle on the down beat, flexed on the upbeat. Kick amplitude should not be 
	exaggerated</li>
	<li>Breathing – Keeping body as low and flat/forward as possible, head tips up enough for face top clear water, inhale, head lays down prior to arm recovery reaching a “T” from the shoulders out to the fingertips</li>
</ul>

