<h4 id="goggles">Choosing the right Swimming goggles:</h4>
<p>Prolonged exposure to chlorine can cause start to eat away at the corneal surface of the eye. This may cause itchy, irritated eyes, or temporary blindness.Contact lens wearers fear the loss of contact lenses, and often swim their eyes closed. If you wear a goggle, you can open your eyes, and not worry about losing a lens. Also, contact lenses may protect you from the above mentioned problems, but they actually increase the risk of one getting an infection from microorganisms in the water.Therefore, swimming goggles can protect your eyes from these ailments. Also, when swimming outdoors, swim goggles can protect your eyes from ultraviolet radiation, and they can also have a tinted lens to shield you from the sun. If you wear a prescription, swim goggles can be fit with your prescription in them to help you see underwater.</p>
<p>Here's what to look for:</p>
<ul>
	<li><b>Ultraviolet Protection:</b> If you are swimming outdoors, make sure your goggle protects you from ultraviolet radiation. (For more on UV, see UV and its Effects on the Eyes).</li>
	
	<li><b>Proper Seal:</b> The most important thing is selecting a goggle is a proper seal. For a goggle to properly work, it must create a good seal so water cannot seep into the goggle. You want the goggle to fit on the bone around the eyes, not inside the eye socket itself. An improperly fit goggle will cause "raccoon eyes". This is because the goggle rests on the socket, instead of the bone, and cuts off circulation. Also, the goggle must seal completely. Put the goggle on and make sure you can see no visible gaps between the goggle and your face. If making a prescription in the lens, you may want to take the goggle home first, and make sure it seals properly before you go through the expense of a prescription lens.</li>
	
	<li><b>Lens Color:</b> If swimming outdoors, you may want to put a slight tint in the lens to help block the amount of sunlight getting to your eyes. A smoke lens is still light enough to see underwater, but can block enough light so you don't have to squint.</li>
</ul>

